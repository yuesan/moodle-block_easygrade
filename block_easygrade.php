defined('MOODLE_INTERNAL') || die();

class block_easygrade extends block_base {

    function init() {
        $this->title = get_string('pluginname', 'block_easygrade');
    }

    function get_content() {
        global $CFG, $OUTPUT;

        if ($this->content !== null) {
            return $this->content;
        }

        if (empty($this->instance)) {
            $this->content = '';
            return $this->content;
        }

        $this->content = new stdClass();
        $this->content->items = array();
        $this->content->icons = array();
        $this->content->footer = '';

        $courseid = $this->page->course->id;
        $context = context_course::instance($courseid);

        if(has_capability("moodle/grade:viewall", $context)){
            $html = html_writer::link(new moodle_url($CFG->wwwroot . "/blocks/easygrade/index.php", ["courseid" => $courseid]), "かんたん評点を起動する", ["class" => "btn btn-success"]);
        }else{
            $html = "";
        }

        return $this->content = (object)['text' => $html];
    }

    // my moodle can only have SITEID and it's redundant here, so take it away
    public function applicable_formats() {
        return array('all' => false,
                     'site' => true,
                     'site-index' => true,
                     'course-view' => true, 
                     'course-view-social' => false,
                     'mod' => true, 
                     'mod-quiz' => false);
    }

    public function instance_allow_multiple() {
          return true;
    }

    function has_config() {return true;}

    public function cron() {
            mtrace( "Hey, my cron script is running" );
             
                 // do something
                  
                      return true;
    }
}
